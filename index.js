const box = document.querySelector('.contenido');
function crearTablas() {
  box.innerHTML = '';

  // Obtener todas las claves únicas
  const todasLasClaves = new Set();
  for (const institucion in datos) {
    for (const clave in datos[institucion]) {
      todasLasClaves.add(clave);
    }
  }
  const clavesArray = Array.from(todasLasClaves).sort();

  // Crear el dropdown
  const label = document.createElement('label');
  label.textContent = 'Selecciona una clave: ';
  label.style.fontWeight = 'bold';
  label.style.marginRight = '8px';

  const select = document.createElement('select');
  select.style.fontSize = '16px';
  select.style.padding = '4px 8px';
  select.style.marginBottom = '16px';

  const optionDefault = document.createElement('option');
  optionDefault.value = '';
  optionDefault.textContent = '-- Selecciona una clave --';
  select.appendChild(optionDefault);

  clavesArray.forEach(clave => {
    const option = document.createElement('option');
    option.value = clave;
    option.textContent = clave;
    select.appendChild(option);
  });

  // Contenedor para la tabla y el h1
  const resultadoDiv = document.createElement('div');
  resultadoDiv.style.marginTop = '24px';

  // Evento para mostrar la tabla al seleccionar una clave
  select.addEventListener('change', function() {
    resultadoDiv.innerHTML = '';
    const claveSeleccionada = select.value;
    if (!claveSeleccionada) return;

    // Crear h1 con la clave seleccionada
    const h1 = document.createElement('h1');
    h1.textContent = claveSeleccionada;
    h1.style.fontSize = '2rem';
    h1.style.marginBottom = '16px';
    resultadoDiv.appendChild(h1);

    // Crear contenedor scrolleable
    const scrollDiv = document.createElement('div');
    scrollDiv.style.maxHeight = '60vh';
    scrollDiv.style.overflowY = 'auto';
    scrollDiv.style.margin = '0 auto';
    scrollDiv.style.width = '90%';

    // Crear la tabla
    const tabla = document.createElement('table');
    tabla.style.background = '#fff';
    tabla.style.color = '#222';
    tabla.style.borderCollapse = 'collapse';
    tabla.style.fontSize = '16px';
    tabla.style.width = '100%';
    tabla.style.minWidth = '500px';

    // Encabezado
    const thead = document.createElement('thead');
    const trHead = document.createElement('tr');
    const thInst = document.createElement('th');
    thInst.textContent = 'Institución';
    thInst.style.padding = '8px';
    thInst.style.border = '1px solid #222';
    thInst.style.background = '#f5f5f5';
    const thContenido = document.createElement('th');
    thContenido.textContent = 'Contenido';
    thContenido.style.padding = '8px';
    thContenido.style.border = '1px solid #222';
    thContenido.style.background = '#f5f5f5';
    trHead.appendChild(thInst);
    trHead.appendChild(thContenido);
    thead.appendChild(trHead);
    tabla.appendChild(thead);

    // Cuerpo de la tabla
    const tbody = document.createElement('tbody');
    for (const institucion in datos) {
      const valor = (datos[institucion][claveSeleccionada] || '').trim();
      if (valor !== '') {
        const tr = document.createElement('tr');
        const tdInst = document.createElement('td');
        tdInst.textContent = institucion;
        tdInst.style.padding = '8px';
        tdInst.style.border = '1px solid #222';
        tdInst.style.background = '#fafafa';
        const tdContenido = document.createElement('td');
        tdContenido.textContent = valor;
        tdContenido.style.padding = '8px';
        tdContenido.style.border = '1px solid #222';
        tdContenido.style.maxWidth = '60vw';
        tdContenido.style.overflow = 'hidden';
        tdContenido.style.textOverflow = 'ellipsis';
        tdContenido.style.whiteSpace = 'pre-line';
        tr.appendChild(tdInst);
        tr.appendChild(tdContenido);
        tbody.appendChild(tr);
      }
    }
    tabla.appendChild(tbody);
    scrollDiv.appendChild(tabla);
    resultadoDiv.appendChild(scrollDiv);
  });

  // Agregar el label, select y resultadoDiv al box
  box.appendChild(label);
  box.appendChild(select);
  box.appendChild(resultadoDiv);
}

function obtenerFrecuenciaAbsoluta() {
  // Limpiar el contenido anterior
  box.innerHTML = '';

  // Contar la frecuencia de cada clave
  const frecuencia = {};
  for (const institucion in datos) {
    for (const clave in datos[institucion]) {
      frecuencia[clave] = (frecuencia[clave] || 0) + 1;
    }
  }

  // Crear un contenedor scrolleable para la tabla
  const scrollDiv = document.createElement('div');
  scrollDiv.style.maxHeight = '80vh';
  scrollDiv.style.overflowY = 'auto';
  scrollDiv.style.margin = '0 auto';
  scrollDiv.style.width = 'fit-content';

  // Crear la tabla
  const tabla = document.createElement('table');
  tabla.style.background = '#fff';
  tabla.style.color = '#222';
  tabla.style.borderCollapse = 'collapse';
  tabla.style.fontSize = '18px';

  // Encabezado
  const thead = document.createElement('thead');
  const trHead = document.createElement('tr');
  const thClave = document.createElement('th');
  thClave.textContent = 'Clave';
  thClave.style.padding = '8px';
  thClave.style.border = '1px solid #222';
  const thFrecuencia = document.createElement('th');
  thFrecuencia.textContent = 'Frecuencia Absoluta';
  thFrecuencia.style.padding = '8px';
  thFrecuencia.style.border = '1px solid #222';
  trHead.appendChild(thClave);
  trHead.appendChild(thFrecuencia);
  thead.appendChild(trHead);
  tabla.appendChild(thead);

  // Cuerpo de la tabla
  const tbody = document.createElement('tbody');
  for (const clave in frecuencia) {
    const tr = document.createElement('tr');
    const tdClave = document.createElement('td');
    tdClave.textContent = clave;
    tdClave.style.padding = '8px';
    tdClave.style.border = '1px solid #222';
    const tdFrecuencia = document.createElement('td');
    tdFrecuencia.textContent = frecuencia[clave];
    tdFrecuencia.style.padding = '8px';
    tdFrecuencia.style.border = '1px solid #222';
    tr.appendChild(tdClave);
    tr.appendChild(tdFrecuencia);
    tbody.appendChild(tr);
  }
  tabla.appendChild(tbody);

  // Agregar la tabla al contenedor scrolleable
  scrollDiv.appendChild(tabla);
  // Agregar el contenedor scrolleable al box
  box.appendChild(scrollDiv);
}
